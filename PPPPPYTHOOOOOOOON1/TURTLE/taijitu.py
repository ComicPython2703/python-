# coding=utf-8
import turtle
t = turtle.Turtle(visible=False)
t.width(5)
t.showturtle()
t.penup()
t.goto(0, -200)
t.pd()
t.fillcolor("black")
t.speed(0)
t.begin_fill()
t.circle(100, 180)
t.circle(-100, 180)
t.circle(-200, -180)
t.end_fill()
t.circle(-200, -180)
t.setheading(-90)
t.penup()
t.forward(100)
t.dot(50, "black")
t.forward(200)
t.dot(50, "white")
t.pd()
t.hideturtle()
turtle.done()
